static rt_err_t bmi08x_read_reg(rt_sensor_t sensor, rt_uint8_t reg_addr, rt_uint8_t* reg_data, rt_size_t len)
{
    rt_err_t result=RT_EOK;

    struct rt_spi_device *spi_dev=RT_NULL;
    spi_dev=(struct rt_spi_device *)rt_device_find(sensor->config.intf.dev_name);
    if(spi_dev==RT_NULL)
    {
        result=-RT_EINVAL;
        goto _exit;
    }

    rt_uint8_t send_buf[2]={0x80|reg_addr,0x00};

    if((sensor->info.type==RT_SENSOR_CLASS_ACCE)||(sensor->info.type==RT_SENSOR_CLASS_TEMP))
        result=rt_spi_send_then_recv(spi_dev,send_buf,2,reg_data,len);
    else if(sensor->info.type==RT_SENSOR_CLASS_GYRO)
        result=rt_spi_send_then_recv(spi_dev,send_buf,1,reg_data,len);
    else
        result=-RT_EINVAL;

_exit:
    return result;
}

static rt_err_t bmi08x_write_reg(rt_sensor_t sensor, rt_uint8_t reg_addr, rt_uint8_t* reg_data, rt_size_t len)
{
    rt_err_t result=RT_EOK;

    struct rt_spi_device *spi_dev=RT_NULL;
    spi_dev=(struct rt_spi_device *)rt_device_find(sensor->config.intf.dev_name);
    if(spi_dev==RT_NULL)
    {
        result=-RT_EINVAL;
        goto _exit;
    }

    result=rt_spi_send_then_send(spi_dev,&reg_addr,1,reg_data,len);

_exit:
    return result;
}
