rt_err_t bmi08x_load_config_file(struct rt_spi_device *device,const rt_uint8_t* file_ptr,rt_size_t file_size)
{
    volatile rt_err_t result=RT_EOK;
    rt_uint8_t send_buf[3]={0};
    rt_uint8_t recv_buf[3]={0};

    const rt_uint16_t block_size=32;

    volatile struct rt_spi_message reg_addr, msg_file,ret;
    struct rt_spi_message * pret=&ret;

    //停止配置加载
    send_buf[0]=BMI08x_INIT_CTRL_ADDR;
    send_buf[1]=0;
    result=rt_spi_transfer(device,send_buf,RT_NULL,2);

    /*
    FEATURES_LSB FEATURES_MSB猜测节构如下：
     M             L
    ┌─┬─┬─┬─┬─┬─┬─┬─┐
    │?│?│9│8│7│6│5│4│msb
    ├─┼─┼─┼─┼─┼─┼─┼─┤
    │x│x│x│x│3│2│1│0│lsb
    └─┴─┴─┴─┴─┴─┴─┴─┘
    这两个寄存器配置写入bmi08x的ram的数据块大小，lsb的高四位不使用，msb最高两位
    不知道是否有效，msb_lsb的值为写入块大小的一半，也就是说假如写入块大小为32字
    节，向msb_lsb寄存器写入16，然后连续向FEATURES_IN写入32字节的配置文件，之后
    重复msb_lsb写入16以及FEATURES_IN写入32字节的操作，直到配置文件传输完成。
    */

    for (rt_size_t i = 0; i < file_size; i+=block_size)
    {

        ret.send_buf=send_buf;
        ret.recv_buf=RT_NULL;
        ret.length=1;
        ret.cs_take=1;
        ret.cs_release=0;
        ret.next=RT_NULL;

        //需要做分块传输
        //写入配置文件长度
        send_buf[0]=BMI08x_FEATURES_LSB_ADDR;
        send_buf[1]=(block_size>>1)&0xf;//block_size/2
        result=rt_spi_transfer(device,send_buf,RT_NULL,2);while(result!=2);

        send_buf[0]=BMI08x_FEATURES_MSB_ADDR;
        send_buf[1]=block_size>>5;//block_size/2>>4
        result=rt_spi_transfer(device,send_buf,RT_NULL,2);while(result!=2);

        //向FEATURE_IN写入配置文件
        send_buf[0]=BMI08x_FEATURES_IN_ADDR;
        reg_addr.send_buf=send_buf;
        reg_addr.recv_buf=RT_NULL;
        reg_addr.length=1;
        reg_addr.cs_take=1;
        reg_addr.cs_release=0;
        reg_addr.next=&msg_file;

        msg_file.send_buf=&file_ptr[i];
        msg_file.recv_buf=RT_NULL;
        msg_file.length=block_size;
        msg_file.cs_take=0;
        msg_file.cs_release=1;
        msg_file.next=RT_NULL;
        pret=rt_spi_transfer_message(device,&reg_addr);

        while(pret!=RT_NULL);
    }

    //开始加载配置
    send_buf[0]=BMI08x_INIT_CTRL_ADDR;
    send_buf[1]=1;
    result=rt_spi_transfer(device,send_buf,RT_NULL,2);while(result!=2);

    //等待150ms
    rt_thread_mdelay(150);

    //读取内部状态
    send_buf[0]=0x80|BMI08x_INTERNAL_STATUS_ADDR;
    send_buf[1]=0;
    send_buf[2]=0;
    result=rt_spi_transfer(device,send_buf,recv_buf,3);while(result!=3);

    while(recv_buf[2]!=0x01);

    return result;
}